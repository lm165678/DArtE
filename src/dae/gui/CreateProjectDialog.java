    private void btnAddFolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFolderActionPerformed
        // TODO add your handling code here:
        int option = directoryChooser.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            File selected = directoryChooser.getSelectedFile();
            assetLocations.add(selected);
        }
        //this.
        lstAssetFolders.setListData(assetLocations.toArray());
        updateButtons();
        okButton.setEnabled(checkInput());
    }//GEN-LAST:event_btnAddFolderActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        File selected = (File) lstAssetFolders.getSelectedValue();
        if (selected != null) {
            assetLocations.remove(selected);
            lstAssetFolders.setListData(assetLocations.toArray());
        }
        updateButtons();
        okButton.setEnabled(checkInput());
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnMoveUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoveUpActionPerformed
        File selected = (File) lstAssetFolders.getSelectedValue();
        if (selected != null) {
            int index = assetLocations.indexOf(selected);
            if (index > 0) {
                File current = assetLocations.get(index - 1);
                assetLocations.set(index - 1, selected);
                assetLocations.set(index, current);
                lstAssetFolders.setListData(assetLocations.toArray());
            }
        }
    }//GEN-LAST:event_btnMoveUpActionPerformed

    private void btnMoveDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoveDownActionPerformed
        File selected = (File) lstAssetFolders.getSelectedValue();
        if (selected != null) {
            int index = assetLocations.indexOf(selected);
            if (index + 1 < assetLocations.size()) {
                File current = assetLocations.get(index + 1);
                assetLocations.set(index + 1, selected);
                assetLocations.set(index, current);
                lstAssetFolders.setListData(assetLocations.toArray());
            }
        }
    }//GEN-LAST:event_btnMoveDownActionPerformed

    private void btnChangeProjectLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeProjectLocationActionPerformed
        int option = directoryChooser.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            this.projectLocation = directoryChooser.getSelectedFile();
            okButton.setEnabled(checkInput());
            this.txtLocation.setText(projectLocation.getPath());
        }
    }//GEN-LAST:event_btnChangeProjectLocationActionPerformed

