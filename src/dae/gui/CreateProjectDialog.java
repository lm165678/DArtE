    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (checkInput()) {
            this.result = new Project();
            result.setProjectName(txtName.getText());
            result.setProjectLocation(this.projectLocation);
            for (File assetFolder : this.assetLocations) {
                result.addAssetFolder(assetFolder);
            }

            if (cboCreateLevel.isSelected()) {
                result.addLevel(txtLevelName.getText());
            }
            doClose(RET_OK);
        }
    }//GEN-LAST:event_okButtonActionPerformed

