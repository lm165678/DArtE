    private void updateButtons() {
        btnRemove.setEnabled(assetLocations.size() > 0);
        btnMoveUp.setEnabled(assetLocations.size() > 1);
        btnMoveDown.setEnabled(assetLocations.size() > 1);
    }

    public void clear() {

        txtLevelName.setText("");
        cboCreateLevel.setSelected(false);
        assetLocations.clear();
        lstAssetFolders.setListData(assetLocations.toArray());
        checkInput();
        updateButtons();
    }

    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }

