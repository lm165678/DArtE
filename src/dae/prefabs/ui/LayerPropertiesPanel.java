    public void setModel(Layer layer){
        this.model = layer;
        cboVisible.setSelected(layer.isVisible());
        cboLocked.setSelected(layer.isLocked());
        txtLayerName.setText(layer.getName());
        
        txtNumChildren.setText( Integer.toString(layer.getNrOfNodes()));
        txtNumChildLayers.setText(Integer.toString(layer.getNrOfLayers()));
    }
    
    public Layer getModel(){
        return model;
    }
    
    @Subscribe
    public void layerChanged(LayerEvent le){
        if ( le.getEventType() == LayerEventType.SELECTED)
        {
            this.setModel(le.getLayer());
        }
    }

